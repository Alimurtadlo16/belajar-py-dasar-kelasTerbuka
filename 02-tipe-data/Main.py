#Contoh tipe data dasar di Python

#Tipe data integer
int = 99
print("Isi data:", int, ", bertipe:", type(int))

#Tipe data float
float = 99.99
print("Isi data:", float, ", bertipe:", type(float))

#Tipe data string
string = "Hello My Name Is Ahmad Ali Murtadlo Asadillah"
print("Isi data:", string, ", bertipe:", type(string))

#Tipe data boolean
boolean = True
print("Isi data:", boolean, ", bertipe:", type(boolean))

