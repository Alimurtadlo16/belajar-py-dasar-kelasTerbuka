# Dictionary dalam python

# List array akses dengan index
dataList = ["Ali", "Joseph", "Steven"]
print(dataList[0])

# Identifier - key  berguna untuk struktur data
dataDict = {
    'Al':'Ali', 
    'Js':'Joseph', 
    'st':'Steven', 
    'nmbr':'99', 
    'list': dataList,
}
print(dataDict['Js'])
print(dataDict['nmbr'])
print(dataDict['list'])